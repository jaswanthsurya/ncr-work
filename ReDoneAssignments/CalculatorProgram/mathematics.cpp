#include "Mathematics.h"
int Mathematics::add(int num1, int num2) {
	return Mathematics::result = num1 + num2;
}
int Mathematics::sub(int num1, int num2) {
	return Mathematics::result = num1 - num2;
}
int Mathematics::mul(int num1, int num2) {
	return Mathematics::result = num1 * num2;
}
int Mathematics::div(int num1, int num2) {
	return Mathematics::result = num1 / num2;
}